package curso.java.ryan;

public class operadoresRelacionais {
	public static void main(String[] args) {
		
	int nota1 = 7;
	int nota2 = 6;
	
	if(nota1 == nota2) {
		System.out.println("As notas são iguais.");
	}
	else {
		System.out.println("As notas são diferentes");
	}
	if(nota1 > nota2) {
		System.out.println("A nota1 é maior que a nota2.");
	}
	else {
		System.out.println("A nota 1 é menor que a nota2");
	}
}
}
